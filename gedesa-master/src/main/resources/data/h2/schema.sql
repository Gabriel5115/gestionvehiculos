CREATE TABLE COMERCIALES(

	ID					INTEGER				NOT NULL,
	NOMBRE				VARCHAR(200)		,
	APELLIDO1			VARCHAR(200)		,
	APELLIDO2			VARCHAR(200)		,
	
	PRIMARY KEY(ID)
);

CREATE TABLE CLIENTES(

	ID					VARCHAR(30)			NOT NULL,
	NOMBRE_COMERCIAL	VARCHAR(250)		,
	NOMBRE				VARCHAR(100)		,
	APELLIDO1			VARCHAR(100)		,
	APELLIDO2			VARCHAR(100)		,
	TELEFONO			VARCHAR(30)			,
	MOVIL				VARCHAR(30)			,
	EMAIL				VARCHAR(100)		,
	DIRECCION			VARCHAR(100)		,
	POBLACION			VARCHAR(100)		,
	CODIGO_POSTAL		VARCHAR(100)		,
	PROVINCIA			VARCHAR(100)		,
	PAIS				VARCHAR(100)		,
		
	PRIMARY KEY(ID)
);

CREATE TABLE PRODUCTOS(

	CODIGO				INTEGER				NOT NULL,
	NOMBRE				VARCHAR(100)		,
	FECHA_ALTA			DATE				,
	PRECIO				DOUBLE				,
	FAMILIA				VARCHAR(20)			,
	DESCATALOGADO		BOOLEAN				,
		
	PRIMARY KEY(CODIGO)
);

CREATE TABLE PEDIDOS(

	CODIGO 				BIGINT				NOT NULL,
	FECHA_HORA			TIMESTAMP			,
	ID_COMERCIAL		INTEGER				NOT NULL,
	ID_CLIENTE			VARCHAR(30)			NOT NULL,
	OBSERVACIONES		VARCHAR(200)		,
	
	PRIMARY KEY(CODIGO),
	FOREIGN KEY(ID_COMERCIAL) REFERENCES COMERCIALES(ID),
	FOREIGN KEY(ID_CLIENTE) REFERENCES CLIENTES(ID)
);


CREATE TABLE LINEAS_PEDIDOS(
	
	CODIGO_PEDIDO		BIGINT					NOT NULL,
	CODIGO_PRODUCTO		INTEGER					NOT NULL,
	CANTIDAD			INTEGER					,
	
	
	FOREIGN KEY(CODIGO_PRODUCTO) REFERENCES PRODUCTOS(CODIGO),
	FOREIGN KEY(CODIGO_PEDIDO) REFERENCES PEDIDOS(CODIGO)

);

CREATE TABLE SECUENCIAS(

	IDS			VARCHAR(100)					NOT NULL,
	VALOR			BIGINT						NOT NULL

);



